export function createModalEventBinder(deps) {
    const {
        bindModalBasicEventsUI,
        bindSettingEventsUI,
        bindCollapsePanelEventsUI,
        bindPromptEventsUI,
        bindPromptEditorEvents,
        bindMessageChainEventsUI,
        bindFileEventsUI,
        bindActionEventsUI,
        bindStreamEventsUI,
        bindExportEventsUI,
        EventDelegate,
        AppState,
        ErrorHandler,
        closeModal,
        showHelpModal,
        getEscKeyHandler,
        saveCurrentSettings,
        handleUseTavernApiChange,
        handleProviderChange,
        switchApiTab,
        handleFetchModels,
        handleQuickTest,
        rechunkMemories,
        showAddCategoryModal,
        saveCustomCategories,
        confirmAction,
        resetToDefaultCategories,
        renderCategoriesList,
        renderCategoryGuidePromptEditors,
        showAddDefaultEntryModal,
        saveDefaultWorldbookEntriesUI,
        applyDefaultWorldbookEntries,
        showResultSection,
        updateWorldbookPreview,
        testChapterRegex,
        renderMessageChainUI,
        handleFileSelect,
        handleClearFile,
        handleStartConversion,
        handleStartDirectorConversion,
        handleStopProcessing,
        handleRepairFailedMemories,
        showStartFromSelector,
        showProcessedResults,
        toggleMultiSelectMode,
        deleteSelectedMemories,
        updateMemoryQueueUI,
        showSearchModal,
        showReplaceModal,
        showWorldbookView,
        showHistoryView,
        showConsolidateCategorySelector,
        showCleanTagsModal,
        showBatchDeleteRepeatedSegmentsModal,
        showAliasMergeUI,
        showTxtConverterPanel,
        showProgressPanel,
        showStoryOutlinePanel,
        showCurrentChapterPanel,
        showSettingsPanel,
        updateStreamContent,
        showPromptPreview,
        showPlotOutlineConfigModal,
        previewRepeatedSegmentsCleanup,
        executeRepeatedSegmentsCleanup,
        importAndMergeWorldbook,
        importAndMergeCharacterCard,
        restoreTaskSnapshot,
        loadTaskState,
        saveTaskState,
        exportSettings,
        importSettings,
        exportCharacterCard,
        exportVolumes,
        exportToSillyTavern,
        showMemoryContentModal,
        handlePluginSelfUpdate,
    } = deps;

    function bindModalEvents(modalContainer) {
        bindModalBasicEventsUI({
            modalContainer,
            closeModal,
            showHelpModal,
            handleEscKey: getEscKeyHandler(),
        });

        bindSettingEventsUI({
            EventDelegate,
            modalContainer,
            AppState,
            saveCurrentSettings,
            handleUseTavernApiChange,
            handleProviderChange,
            switchApiTab,
            handleFetchModels,
            handleQuickTest,
            rechunkMemories,
            showAddCategoryModal,
            saveCustomCategories,
            confirmAction,
            resetToDefaultCategories,
            renderCategoriesList,
            renderCategoryGuidePromptEditors,
            showAddDefaultEntryModal,
            saveDefaultWorldbookEntriesUI,
            applyDefaultWorldbookEntries,
            showResultSection,
            updateWorldbookPreview,
            ErrorHandler,
            testChapterRegex,
            handlePluginSelfUpdate,
        });

        bindCollapsePanelEventsUI();
        bindPromptEventsUI({ saveCurrentSettings });
        if (typeof bindPromptEditorEvents === 'function') {
            bindPromptEditorEvents({ modalContainer, EventDelegate });
        }
        bindMessageChainEventsUI({
            AppState,
            renderMessageChainUI,
            saveCurrentSettings,
            confirmAction,
        });
        bindFileEventsUI({
            AppState,
            ErrorHandler,
            confirmAction,
            handleFileSelect,
            handleClearFile,
            previewRepeatedSegmentsCleanup,
            executeRepeatedSegmentsCleanup,
        });
        bindActionEventsUI({
            AppState,
            handleStartConversion,
            handleStartDirectorConversion,
            handleStopProcessing,
            handleRepairFailedMemories,
            showStartFromSelector,
            showProcessedResults,
            toggleMultiSelectMode,
            deleteSelectedMemories,
            updateMemoryQueueUI,
            showSearchModal,
            showReplaceModal,
            showWorldbookView,
            showHistoryView,
            showConsolidateCategorySelector,
            showCleanTagsModal,
            showAliasMergeUI,
            showTxtConverterPanel,
            showProgressPanel,
            showStoryOutlinePanel,
            showCurrentChapterPanel,
            showSettingsPanel,
        });
        bindStreamEventsUI({ updateStreamContent });
        bindExportEventsUI({
            AppState,
            showPromptPreview,
            showPlotOutlineConfigModal,
            showBatchDeleteRepeatedSegmentsModal,
            importAndMergeWorldbook,
            importAndMergeCharacterCard,
            loadTaskState,
            saveTaskState,
            exportSettings,
            importSettings,
            exportCharacterCard,
            exportVolumes,
            exportToSillyTavern,
            showMemoryContentModal,
        });
    }

    return {
        bindModalEvents,
    };
}
